# Classes and objects

class Person:
    name = "Kevin"
    age = 26
    gender = "Male"

person1 = Person()

print(person1.name)
print(person1.age)
print(person1.gender)

person2 = Person()

print(person2.name)
print(person2.age)
print(person2.gender)

person2.name = "Jake"
person2.age = 28

print(person2.name)
print(person2.age)
print(person2.gender)

    

